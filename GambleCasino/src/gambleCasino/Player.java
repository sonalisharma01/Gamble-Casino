package gambleCasino;
public class Player {
	private String name;   //name declaration
	private int age;		//age declaration
	
	 //getters and setters declared 
	public void setName(String name)
	{
		this.name = name;		
	}
	
	public void setAge(int age)
	{
		this.age = age;		
	}
	
	public String getName()
	{		return name;
	
	}
	public int getAge()
	{
		return age;		
	}
	
}